//if-else condition.
let age=19;

if(age>=18){
  console.log("Drive a car");
}
else{
  console.log("not drive car")
}


//condition(Ternary) operator.

let text=(age>=18)?"Drive car":"not drive car";


//switch condition.

let day=3;

switch(day){
  case 1:
    console.log("monday");
    break;

    case 2:
      console.log("tuesday");
      break;

    case 3:
      console.log("wednesday");
      break;
      
    default:
      console.log("invalid day");
      break;

}

